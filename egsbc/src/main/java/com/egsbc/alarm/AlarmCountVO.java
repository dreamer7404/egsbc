package com.egsbc.alarm;

public class AlarmCountVO {
	private int criticalCnt;
	private int majorCnt;
	private int minorCnt;
	private int informationCnt;
	
	public int getCriticalCnt() {
		return criticalCnt;
	}
	public void setCriticalCnt(int criticalCnt) {
		this.criticalCnt = criticalCnt;
	}
	public int getMajorCnt() {
		return majorCnt;
	}
	public void setMajorCnt(int majorCnt) {
		this.majorCnt = majorCnt;
	}
	public int getMinorCnt() {
		return minorCnt;
	}
	public void setMinorCnt(int minorCnt) {
		this.minorCnt = minorCnt;
	}
	public int getInformationCnt() {
		return informationCnt;
	}
	public void setInformationCnt(int informationCnt) {
		this.informationCnt = informationCnt;
	}
	
	
	
}
